print("Operadores de comparacion en Language App")

puntos_usuario = float(input("Puntos del usuario: "))
puntos_meta = float(input("Puntos meta: "))

print(puntos_usuario, "==", puntos_meta, puntos_usuario == puntos_meta)
print(puntos_usuario, "!=", puntos_meta, puntos_usuario != puntos_meta)
print(puntos_usuario, ">", puntos_meta, puntos_usuario > puntos_meta)
print(puntos_usuario, "<", puntos_meta, puntos_usuario < puntos_meta)
print(puntos_usuario, ">=", puntos_meta, puntos_usuario >= puntos_meta)
print(puntos_usuario, "<=", puntos_meta, puntos_usuario <= puntos_meta)
